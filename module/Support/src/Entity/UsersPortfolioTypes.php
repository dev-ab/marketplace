<?php

namespace Support\Entity;

class UsersPortfolioTypes {

    protected $id;
    protected $type;

    public function setId($id) {
        $this->id = $id;
        return $this;
    }

    public function getId() {
        return $this->id;
    }

    public function setType($type) {
        $this->type = $type;
        return $this;
    }

    public function getType() {
        return $this->type;
    }

}
